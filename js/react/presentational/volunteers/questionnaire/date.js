import {h} from 'react-markup'
import React, {PropTypes} from 'react'
import prefixer from 'bem-prefixer'
import DatePicker from 'react-datepicker'
import moment from 'moment'

import QuestionTitle from './elements/question-title'

const bem = prefixer('questionnaire-date')

const DATE_FORMAT = 'DD/MM/YYYY' // todo: need to get from i18n object

//todo: really bad! need to pass i18n by context
let locale = 'ru'
if (window.location.pathname.startsWith('/es')) {
    locale = 'es'
}
else if (window.location.pathname.startsWith('/en')) {
    locale = 'en'
}


class Text extends React.Component {

    handleChange = (m) => {
        const {onChange} = this.props
        onChange(m.format(DATE_FORMAT))
    }

    render() {
        moment.locale('es')
        const {name, title, value} = this.props
        const valueObj = value === ''
            ? moment()
            : moment(value, DATE_FORMAT) // todo: default value passed to component should be reasonable
        return h(bem('div'),
            h(bem('label#label'),
                h(QuestionTitle, title),
                h(DatePicker, {
                    className: 'questionnaire-date__input',
                    selected: valueObj,
                    dateFormat: DATE_FORMAT,
                    onChange: this.handleChange,
                    showYearDropdown: true,
                    locale,
                })
            )
        )
    }
}

Text.propTypes = {
    name: PropTypes.string.isRequired,
    title: PropTypes.string.isRequired,
    value: PropTypes.string.isRequired,
    onChange: PropTypes.func.isRequired,
    mandatory: PropTypes.bool,
}

Text.defaultType = {
    mandatory: false,
}

export default Text

